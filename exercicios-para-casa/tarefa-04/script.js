/**
    Tarefa 04

    1. Receba o preço de cada um dos 10 itens;
    2. Mostre na tela a soma de todos os preços.
 */

const quantidade = 10;
var total = 0;
for (let i = 1; i <= quantidade; i++) {
  var preco = Number(prompt("Digite o valor dos itens"));
  var total = total + preco;
}
if ((i = 10)) {
  alert(total);
}
